export const getFilteredCities = (query: string): string[] => {
  if (query.length < 3) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  return popularCities
    .filter(city => city.toLowerCase().includes(normalizedQuery))
    .sort((a, b) => {
      // Prioritize cities that start with the query
      const aStartsWith = a.toLowerCase().startsWith(normalizedQuery);
      const bStartsWith = b.toLowerCase().startsWith(normalizedQuery);
      
      if (aStartsWith && !bStartsWith) return -1;
      if (!aStartsWith && bStartsWith) return 1;
      
      // Then sort alphabetically
      return a.localeCompare(b);
    })
    .slice(0, 8); // Limit to 8 suggestions
};